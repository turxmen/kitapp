class FoodItem {
  String id; // Changed to String for UUID
  String category;
  String name;
  String imageUrl;
  double defaultQuantity;
  String unit;
  String storageLocation;
  int daysToExpiry;
  String colorCode;
  String? subcategory;
  bool isFavorite;
  String? generalSeason;
  bool seasonality;
  String? storageTips;
  List<Map<String, dynamic>> bestUsedIn; // Added bestUsedIn field

  FoodItem({
    required this.id,
    required this.category,
    required this.name,
    required this.imageUrl,
    required this.defaultQuantity,
    required this.unit,
    required this.storageLocation,
    required this.daysToExpiry,
    required this.colorCode,
    this.subcategory,
    this.isFavorite = false,
    this.generalSeason,
    this.seasonality = true,
    this.storageTips,
    this.bestUsedIn = const [],
  });

  factory FoodItem.fromJson(Map<String, dynamic> json) {
    return FoodItem(
      id: json['id'] as String,
      category: json['category'] as String,
      name: json['name'] as String,
      imageUrl: json['imageUrl'] as String,
      defaultQuantity: (json['defaultQuantity'] as num).toDouble(),
      unit: json['unit'] as String,
      storageLocation: json['storageLocation'] as String,
      daysToExpiry: json['daysToExpiry'] as int,
      colorCode: json['colorCode'] as String,
      subcategory: json['subcategory'] as String?,
      isFavorite: json['isFavorite'] ?? false,
      generalSeason: json['generalSeason'] as String?,
      seasonality: json['seasonality'] ?? true,
      storageTips: json['storageTips'] as String?,
      bestUsedIn: (json['bestUsedIn'] as List<dynamic>?)
              ?.map((e) => e as Map<String, dynamic>)
              .toList() ??
          [], // Added bestUsedIn parsing with null check and default value
    );
  }
}

